/** Quick lookup table for the article detail page. */
export function getPostBySlug(slug: string): BlogPost | undefined {
  return BLOG_POSTS.find((p) => p.slug === slug);
}

/** Recommended posts — same category first, then anything else.  Excludes
 *  the current post.  Used at the bottom of the article detail page. */
export function getRelatedPosts(slug: string, n = 3): BlogPost[] {
  const cur = getPostBySlug(slug);
  if (!cur) return BLOG_POSTS.slice(0, n);
  const sameCat = BLOG_POSTS.filter(
    (p) => p.slug !== slug && p.category === cur.category,
  );
  const others = BLOG_POSTS.filter(
    (p) => p.slug !== slug && p.category !== cur.category,
  );
  return [...sameCat, ...others].slice(0, n);
}

/** Bn formatter shared across the blog UI (digits + lakh/crore grouping). */
export const bnNumber = (n: number) =>
  new Intl.NumberFormat("bn-BD", { maximumFractionDigits: 0 }).format(n);

const BN_MONTHS = [
  "জানুয়ারি", "ফেব্রুয়ারি", "মার্চ", "এপ্রিল", "মে", "জুন",
  "জুলাই", "আগস্ট", "সেপ্টেম্বর", "অক্টোবর", "নভেম্বর", "ডিসেম্বর",
];

/** Format an ISO date (YYYY-MM-DD) as "১৫ মে, ২০২৬" — matches the hand
 *  written `date` strings on the code-defined posts. */
export const bnDate = (iso: string): string => {
  const [y, m, d] = iso.split("-").map(Number);
  if (!y || !m || !d) return iso;
  return `${bnNumber(d)} ${BN_MONTHS[m - 1]}, ${bnNumber(y)}`;
};
