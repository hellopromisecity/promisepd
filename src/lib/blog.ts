export type BlogPost = {
  slug: string;
  title: string;
  excerpt: string;
  category: BlogCategoryKey;
  /** Only set on posts in the "projects" category — drives the
   *  specific-project filter dropdown. */
  project?: BlogProjectKey;
  /** Bangla-localized display date — e.g. "১৫ মে, ২০২৬". */
  date: string;
  /** ISO date for sorting + <time dateTime>. */
  iso: string;
  /** Realistic view counter — bumped manually as posts gain traction. */
  views: number;
  /** Estimated reading time in minutes. */
  readingMinutes: number;
  /** Lucide icon name used as the card's visual stand-in for a hero photo. */
  icon: string;
  accent: "red" | "blue" | "ash";
  /** Exactly one post should be true — it owns the page-1 featured slot. */
  featured: boolean;
  /** Article body — structured so we don't need a markdown parser. */
  intro: string;
  sections: { heading: string; body: string[] }[];
  closing?: string;

  // ── DB-backed posts (admin CMS) ─────────────────────────────────
  // These optional fields only exist on posts loaded from the database
  // (src/lib/blog-db.ts).  Code-defined posts above leave them unset and
  // keep using BLOG_COVER + BLOG_EN + `sections` as before.
  /** Resolved cover URL (overrides the static BLOG_COVER lookup). */
  cover?: string;
  /** Rich HTML body — rendered instead of `sections` when present. */
  bodyHtml?: string;
  /** Rich HTML body, English. */
  bodyHtmlEn?: string;
  /** Per-post English overlays (when there's no BLOG_EN[slug] entry). */
  titleEn?: string;
  excerptEn?: string;
  /** Where the post came from — handy for prev/next + debugging. */
  source?: "code" | "db";
};

